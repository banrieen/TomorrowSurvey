      select.select(rlist, wlist, xlist[, timeout])
    </p>
    <p>
      This is a straightforward interface to the Unix <code class="xref c c-func docutils literal notranslate">select()</code>&#160;system call. The first three arguments are sequences of &#8216;waitable objects&#8217;: either integers representing file descriptors or objects with a parameterless method named <a class="reference internal" href="https://docs.python.org/3.6/library/io.html#io.IOBase.fileno" title="io.IOBase.fileno"><code class="xref py py-meth docutils literal notranslate">fileno()</code></a>&#160;returning such an integer:
    </p>
    <ul class="simple">
      <li>
        <p>
          <em>rlist</em>: wait until ready for reading
        </p>
      </li>
      <li>
        <p>
          <em>wlist</em>: wait until ready for writing
        </p>
      </li>
      <li>
        <p>
          <em>xlist</em>: wait for an &#8220;exceptional condition&#8221; (see the manual page for what your system considers such a condition)
        </p>
